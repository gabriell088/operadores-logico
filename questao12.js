/*
omo você pode verificar se um número é positivo e menor que 50 em 
JavaScript usando operadores lógicos?
*/

var numero = 30; // número a ser verificado

// Verifica se o número é positivo e menor que 50 usando operadores lógicos
if (numero > 0 && numero < 50) {
    console.log("O número é positivo e menor que 50.");
} else {
    console.log("O número não é positivo ou não é menor que 50.");
}

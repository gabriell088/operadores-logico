/*
O que retorna a expressão (true || false) && (false || true)?
*/

/*
(true || false) resulta em verdadeiro, pois o operador || (OR) retorna verdadeiro se pelo menos um dos operandos for verdadeiro, e aqui o primeiro operando é verdadeiro (true).

(false || true) também resulta em verdadeiro, pois novamente o operador || (OR) retorna verdadeiro se pelo menos um dos operandos for verdadeiro, e neste caso o segundo operando é verdadeiro (true).
*/
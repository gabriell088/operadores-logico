/*
 Explique como você pode usar operadores lógicos para verificar se uma 
pessoa tem idade entre 18 e 65 anos em JavaScript.
*/

var idade = 30; // Suponha que a idade da pessoa seja 30 anos

// Verifica se a idade está entre 18 e 65 usando operadores lógicos
if (idade >= 18 && idade <= 65) {
    console.log("A pessoa tem idade entre 18 e 65 anos.");
} else {
    console.log("A pessoa não tem idade entre 18 e 65 anos.");
}

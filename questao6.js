/*
Como você pode verificar se um número está dentro de um intervalo de 
10 a 20 em JavaScript usando operadores lógicos?
*/
 
var numero = 15; // número a ser verificado

// Verifica se o número está dentro do intervalo usando operadores lógicos
if (numero >= 10 && numero <= 20) {
    console.log("O número está dentro do intervalo de 10 a 20.");
} else {
    console.log("O número está fora do intervalo de 10 a 20.");
}

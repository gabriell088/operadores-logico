/*
 O que significa a expressão !(5 !== 5)? 
 5 !== 5 verifica se 5 não é estritamente diferente de 5. Como 5 é igual a 5, esta expressão é falsa.
O operador ! (NOT) inverte o valor da expressão. Ou seja, se a expressão for verdadeira, ! a torna falsa, e se for falsa, ! a torna verdadeira.
Portanto, !(5 !== 5) é o mesmo que verificar se 5 é estritamente igual a 5.

Como 5 é igual a 5, a expressão !(5 !== 5) resulta em verdadeiro
*/